public class Letras {

    public static void mostrarPrimeraLetra(String a, String b) {
        System.out.println(a.charAt(0));
        System.out.println(b.charAt(0));
        System.out.println(a.charAt(0));

    }

    public static void main(String[] args) {

        mostrarPrimeraLetra("Ana", "Miguel");
        mostrarPrimeraLetra("Lui", "Miguel");
        mostrarPrimeraLetra("Juan", "Rita", "Santiago", "Leo");

    }

}
